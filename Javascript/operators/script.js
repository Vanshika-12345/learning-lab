// arithmetic operators
//binary operator
let a = 5;
let b = 3;
console.log(a+b); //sum
console.log(a-b); //subtract
console.log(a*b); //multiplication
console.log(a/b); //divide
console.log(a%b); //modulo
console.log(a**b); //power

//Unary operator
let c = 3;
console.log(--c);
console.log(c++);
console.log(++c);

//Comparison operator
console.log('5'==5); //loose equality
console.log('5'=== 5); //strict equality
console.log(a!==b);
console.log(a!=b);

//Ternary operator
let age = 25;
let status1 = (age >=18) ? 'I can vote' : 'I cannot vote';
console.log(status1);


